#include "ft/libft.h"
#include <sys/time.h>

uint64_t			ft_clock(uint64_t start)
{
	struct timeval	tval;

	gettimeofday(&tval, NULL);
	return (tval.tv_sec * MICRO_SEC + tval.tv_usec - start);
}
