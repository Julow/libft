#include "libft.h"
#include <unistd.h>

int				ft_flush(t_buff *buff)
{
	int				tmp;

	if (buff->i > 0)
	{
		tmp = write(buff->fd, buff->data, buff->i);
		buff->i = 0;
	}
	else
		tmp = 0;
	return (tmp);
}
