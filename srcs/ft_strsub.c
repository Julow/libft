#include "libft.h"

char			*ft_strsub(char const *s, unsigned int start, size_t len)
{
	size_t			i;
	char			*str;

	if (s == NULL || ft_strlen(s) < start)
		return (NULL);
	str = MAL(char, len + 1);
	i = 0;
	while (s[start + i] != '\0' && i < len)
	{
		str[i] = s[start + i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
