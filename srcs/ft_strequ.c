#include "libft.h"

int				ft_strequ(char const *s1, char const *s2)
{
	size_t			i;

	if (s1 == NULL || s2 == NULL)
		return ((s1 == s2) ? 1 : 0);
	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0')
		i++;
	return ((s1[i] == s2[i]) ? 1 : 0);
}
