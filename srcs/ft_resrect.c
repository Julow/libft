#include "libft.h"

void			ft_resrect(t_pt *p1, t_pt *p2)
{
	t_pt			tmp;

	tmp.x = MIN(p1->x, p2->x);
	tmp.y = MIN(p1->y, p2->y);
	p2->x = MAX(p1->x, p2->x);
	p2->y = MAX(p1->y, p2->y);
	*p1 = tmp;
}
