#include "ft/math_vec3.h"

t_vec3			ft_vec3mix(t_vec3 a, t_vec3 b, float mix)
{
	return (VEC3_ADD(VEC3_MUL1(a, mix), VEC3_MUL1(b, 1.f - mix)));
}
